#!/usr/bin/env bash
set -euo pipefail

usage() {
  cat <<'EOF'
Usage: bash scripts/benchmark_compile.sh [--iterations N] [--mode cold|warm|both] [--report PATH]

Measures compile-check time for two local fixtures:
- benchmarks/compile/statum-fixture
- benchmarks/compile/plain-fixture

Modes:
- cold: remove the fixture target directory before every measured run
- warm: prime the target directory once, then measure incremental checks
- both: run both modes (default)

Options:
- --report PATH: write a Markdown report with machine/toolchain context, raw runs,
  averages, and statum/plain ratios. Parent directories are created as needed.
EOF
}

iterations=3
mode="both"
report_path=""

while [[ $# -gt 0 ]]; do
  case "$1" in
    --iterations)
      if [[ $# -lt 2 || "${2:-}" == --* ]]; then
        echo "--iterations requires a value" >&2
        usage >&2
        exit 1
      fi
      iterations="$2"
      shift 2
      ;;
    --mode)
      if [[ $# -lt 2 || "${2:-}" == --* ]]; then
        echo "--mode requires a value" >&2
        usage >&2
        exit 1
      fi
      mode="$2"
      shift 2
      ;;
    --report)
      if [[ $# -lt 2 || "${2:-}" == --* ]]; then
        echo "--report requires a value" >&2
        usage >&2
        exit 1
      fi
      report_path="$2"
      shift 2
      ;;
    -h|--help)
      usage
      exit 0
      ;;
    *)
      echo "Unknown argument: $1" >&2
      usage >&2
      exit 1
      ;;
  esac
done

if ! [[ "$iterations" =~ ^[0-9]+$ ]] || [[ "$iterations" -lt 1 ]]; then
  echo "--iterations must be a positive integer" >&2
  exit 1
fi

case "$mode" in
  cold|warm|both) ;;
  *)
    echo "--mode must be one of: cold, warm, both" >&2
    exit 1
    ;;
esac

repo_root="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"

result_rows=()
mode_names=()
average_rows=()

record_result() {
  local fixture_name="$1"
  local mode_name="$2"
  local run="$3"
  local elapsed_ms="$4"
  result_rows+=("${mode_name}|${fixture_name}|${run}|${elapsed_ms}")
}

record_average() {
  local fixture_name="$1"
  local mode_name="$2"
  local avg_ms="$3"
  average_rows+=("${mode_name}|${fixture_name}|${avg_ms}")

  case " ${mode_names[*]} " in
    *" ${mode_name} "*) ;;
    *) mode_names+=("$mode_name") ;;
  esac
}

average_for() {
  local mode_name="$1"
  local fixture_name="$2"

  for row in "${average_rows[@]}"; do
    IFS='|' read -r row_mode row_fixture row_avg <<<"$row"
    if [[ "$row_mode" == "$mode_name" && "$row_fixture" == "$fixture_name" ]]; then
      printf '%s\n' "$row_avg"
      return 0
    fi
  done

  return 1
}

ratio_for() {
  local mode_name="$1"
  local statum_avg plain_avg

  statum_avg="$(average_for "$mode_name" statum)"
  plain_avg="$(average_for "$mode_name" plain)"

  awk -v statum="$statum_avg" -v plain="$plain_avg" 'BEGIN {
    if (plain == 0) {
      printf "undefined"
    } else {
      printf "%.2f", statum / plain
    }
  }'
}

context_value() {
  local fallback="$1"
  shift
  "$@" 2>/dev/null || printf '%s\n' "$fallback"
}

write_report() {
  local destination="$1"
  local report_date git_commit rustc_version cargo_version kernel cpu_model cpu_count memory_total

  if [[ -z "$destination" ]]; then
    return 0
  fi

  mkdir -p "$(dirname "$destination")"

  report_date="$(context_value unknown date -Iseconds)"
  git_commit="$(context_value unknown git -C "$repo_root" rev-parse --short HEAD)"
  rustc_version="$(context_value unknown rustc -Vv)"
  cargo_version="$(context_value unknown cargo -V)"
  kernel="$(context_value unknown uname -a)"
  cpu_model="$(lscpu 2>/dev/null | sed -n 's/^Model name:[[:space:]]*//p' | head -n 1 || true)"
  cpu_model="${cpu_model:-unknown}"
  cpu_count="$(context_value unknown nproc)"
  memory_total="$(free -h 2>/dev/null | awk '/Mem:/ {print $2}' || true)"
  memory_total="${memory_total:-unknown}"

  {
    printf '# Compile-Time Benchmark Baseline\n\n'
    printf 'Generated by `bash scripts/benchmark_compile.sh --iterations %s --mode %s --report %s`.\n\n' \
      "$iterations" "$mode" "$destination"
    printf '## Context\n\n'
    printf -- '- Date: `%s`\n' "$report_date"
    printf -- '- Git commit: `%s`\n' "$git_commit"
    printf -- '- Kernel: `%s`\n' "$kernel"
    printf -- '- CPU: `%s`\n' "$cpu_model"
    printf -- '- Logical CPUs: `%s`\n' "$cpu_count"
    printf -- '- Memory: `%s`\n' "$memory_total"
    printf -- '- Cargo: `%s`\n' "$cargo_version"
    printf -- '- Rust toolchain:\n\n'
    printf '```text\n%s\n```\n\n' "$rustc_version"
    printf '## Fixtures\n\n'
    printf -- '- Plain fixture: `benchmarks/compile/plain-fixture`\n'
    printf -- '- Statum fixture: `benchmarks/compile/statum-fixture`\n'
    printf -- '- Measurement command inside each run: `CARGO_INCREMENTAL=0 CARGO_TARGET_DIR=<fixture target> cargo check --manifest-path <fixture Cargo.toml> --quiet`\n'
    printf -- '- Cold mode removes the fixture target directory before each measured run.\n'
    printf -- '- Warm mode primes the target directory once, then measures repeated checks.\n\n'
    printf '## Results\n\n'
    printf '| Mode | Fixture | Runs (ms) | Average (ms) | Statum/plain ratio |\n'
    printf '| --- | --- | ---: | ---: | ---: |\n'

    for mode_name in "${mode_names[@]}"; do
      local mode_ratio
      mode_ratio="$(ratio_for "$mode_name")"
      for fixture_name in statum plain; do
        local runs=() avg ratio_cell
        for row in "${result_rows[@]}"; do
          IFS='|' read -r row_mode row_fixture _row_run row_elapsed <<<"$row"
          if [[ "$row_mode" == "$mode_name" && "$row_fixture" == "$fixture_name" ]]; then
            runs+=("$row_elapsed")
          fi
        done
        avg="$(average_for "$mode_name" "$fixture_name")"
        if [[ "$fixture_name" == "statum" ]]; then
          if [[ "$mode_ratio" == "undefined" ]]; then
            ratio_cell="undefined"
          else
            ratio_cell="${mode_ratio}x"
          fi
        else
          ratio_cell="baseline"
        fi
        printf '| %s | %s | %s | %s | %s |\n' \
          "$mode_name" "$fixture_name" "$(IFS=', '; printf '%s' "${runs[*]}")" "$avg" "$ratio_cell"
      done
    done

    printf '\n## Interpretation\n\n'
    for mode_name in "${mode_names[@]}"; do
      local statum_avg plain_avg mode_ratio
      statum_avg="$(average_for "$mode_name" statum)"
      plain_avg="$(average_for "$mode_name" plain)"
      mode_ratio="$(ratio_for "$mode_name")"
      if [[ "$mode_ratio" == "undefined" ]]; then
        printf -- '- `%s`: Statum averaged `%s ms`; plain Rust averaged `%s ms`; observed statum/plain ratio was undefined because the plain average was zero.\n' \
          "$mode_name" "$statum_avg" "$plain_avg"
      else
        printf -- '- `%s`: Statum averaged `%s ms`; plain Rust averaged `%s ms`; observed statum/plain ratio was `%sx`.\n' \
          "$mode_name" "$statum_avg" "$plain_avg" "$mode_ratio"
      fi
    done
    printf '\nThese numbers describe only the checked fixtures on this machine and toolchain. Treat them as a local regression baseline, not a universal compile-time guarantee.\n'
  } >"$destination"

  printf 'Wrote report: %s\n' "$destination"
}

measure_once() {
  local manifest_path="$1"
  local target_dir="$2"
  local label="$3"

  local start_ns end_ns elapsed_ms
  start_ns="$(date +%s%N)"
  CARGO_INCREMENTAL=0 \
  CARGO_TARGET_DIR="$target_dir" \
    cargo check --manifest-path "$manifest_path" --quiet
  end_ns="$(date +%s%N)"
  elapsed_ms="$(( (end_ns - start_ns) / 1000000 ))"
  printf '%s\n' "$elapsed_ms"
}

run_fixture_mode() {
  local fixture_name="$1"
  local manifest_path="$2"
  local mode_name="$3"
  local target_dir="$repo_root/target/compile-bench/${fixture_name}/${mode_name}"
  local total_ms=0

  if [[ "$mode_name" == "warm" ]]; then
    rm -rf "$target_dir"
    echo "Priming ${fixture_name} (${mode_name})..."
    measure_once "$manifest_path" "$target_dir" "${fixture_name}-${mode_name}-prime" >/dev/null
  fi

  for run in $(seq 1 "$iterations"); do
    if [[ "$mode_name" == "cold" ]]; then
      rm -rf "$target_dir"
    fi

    local elapsed_ms
    elapsed_ms="$(measure_once "$manifest_path" "$target_dir" "${fixture_name}-${mode_name}-${run}")"
    total_ms="$((total_ms + elapsed_ms))"
    record_result "$fixture_name" "$mode_name" "$run" "$elapsed_ms"
    printf '%-8s %-4s run %d: %s ms\n' "$fixture_name" "$mode_name" "$run" "$elapsed_ms"
  done

  local avg_ms
  avg_ms="$(( total_ms / iterations ))"
  record_average "$fixture_name" "$mode_name" "$avg_ms"
  printf '%-8s %-4s avg: %s ms\n' \
    "$fixture_name" \
    "$mode_name" \
    "$avg_ms"
}

run_mode() {
  local mode_name="$1"
  run_fixture_mode "statum" "$repo_root/benchmarks/compile/statum-fixture/Cargo.toml" "$mode_name"
  run_fixture_mode "plain" "$repo_root/benchmarks/compile/plain-fixture/Cargo.toml" "$mode_name"
}

if [[ "$mode" == "cold" || "$mode" == "both" ]]; then
  run_mode "cold"
fi

if [[ "$mode" == "warm" || "$mode" == "both" ]]; then
  run_mode "warm"
fi

write_report "$report_path"
